/**
 * @file registry_value.hpp
 * @brief Модель данных значения реестра Windows.
 * @details Определяет структуру для представления значения в реестре Windows,
 * включая его имя, строковое представление данных и тип.
*/

#pragma once

#include <cstdint>
#include <string>

namespace RegistryAnalysis {

/**
 * @struct Value
 * @brief Представление значения в реестре Windows.
 * @details Структура используется для хранения информации о конкретном значении 
 * ключа реестра. Содержит имя, строковые данные и тип значения.
*/
struct Value {
  /**
   * @brief Имя значения.
   * @details Строка в кодировке UTF-8, представляющая имя элемента значения 
   * внутри ключа реестра. Может быть пустой строкой для значения "(по умолчанию)".
  */
  std::string name_;

  /**
   * @brief Данные значения в строковом представлении.
   * @details Содержит строковое представление содержимого значения.
  */
  std::string data_;

  /**
   * @brief Тип значения.
   * @details Числовой идентификатор типа значения согласно формату Windows Registry:
   * - `REG_SZ` (1): строка
   * - `REG_DWORD` (4): 32-битное целое число
   * - `REG_QWORD` (11): 64-битное целое число
   * - и другие типы, определённые в документации Microsoft.
  */
  uint32_t type_;
};

}
