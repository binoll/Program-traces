/**
 * @file registry_value.hpp
 * @brief Модель данных значения реестра Windows
 * @note Определяет структуру для представления значения в реестре Windows,
 *          включая имя, данные и тип значения
 */

#pragma once

#include <cstdint>
#include <string>

namespace RegistryAnalysis {

/**
 * @struct Value
 * @brief Представление значения реестра Windows
 * @note Инкапсулирует все атрибуты значения реестра, предоставляя
 *          структурированный доступ к данным. Поддерживает различные типы
 *          значений через числовой идентификатор типа
 */
struct Value {
  /// @name Основные свойства значения
  /// @{
  /**
   * @brief Имя значения реестра
   * @note Хранится в кодировке UTF-8. Для значения по умолчанию (default)
   *          используется пустая строка
   */
  std::string name_;

  /**
   * @brief Строковое представление данных значения
   * @note Содержит данные, преобразованные в строковый формат:
   * - Для числовых типов (DWORD/QWORD) - десятичное/шестнадцатеричное
   * представление
   * - Для строковых типов (SZ/MULTI_SZ) - исходная строка(и)
   * - Для бинарных данных (BINARY) - hex-представление
   */
  std::string data_;

  /**
   * @brief Тип значения реестра
   * @note Числовой идентификатор типа согласно спецификации Windows:
   * @code
   * 1  (REG_SZ):         Строка
   * 2  (REG_EXPAND_SZ):  Строка с переменными окружения
   * 3  (REG_BINARY):     Бинарные данные
   * 4  (REG_DWORD):      32-битное целое
   * 7  (REG_MULTI_SZ):   Многострочная строка
   * 11 (REG_QWORD):      64-битное целое
   * @endcode
   */
  uint32_t type_;
  /// @}
};

}
